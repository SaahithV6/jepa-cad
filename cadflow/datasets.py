def match_dataset_sources(keywords: tuple[str, ...]) -> list[DatasetSource]:
    lowered = {keyword.lower() for keyword in keywords}
    ranked: list[DatasetSource] = []
    for source in DATASET_REGISTRY.values():
        if source.domain in lowered or lowered.intersection(source.recommended_for):
            ranked.append(source)
            continue
        haystack = " ".join((source.key, source.title, source.domain, source.notes, " ".join(source.use_cases))).lower()
        if any(keyword in haystack for keyword in lowered):
            ranked.append(source)
    return ranked


def registry_to_dict() -> dict[str, dict[str, Any]]:
    return {key: source.to_dict() for key, source in DATASET_REGISTRY.items()}
