"""Curated public dataset registry for space + mechanical CAD training."""

from __future__ import annotations

from dataclasses import asdict, dataclass
from typing import Any


@dataclass(frozen=True, slots=True)
class DatasetSource:
    key: str
    title: str
    domain: str
    url: str
    license: str
    use_cases: tuple[str, ...]
    notes: str = ""
    size_hint: str = ""
    recommended_for: tuple[str, ...] = ()

    def to_dict(self) -> dict[str, Any]:
        return asdict(self)


def _ds(
    key: str,
    title: str,
    domain: str,
    url: str,
    license: str,
    use_cases: tuple[str, ...],
    notes: str = "",
    size_hint: str = "",
    recommended_for: tuple[str, ...] = (),
) -> DatasetSource:
    return DatasetSource(
        key=key,
        title=title,
        domain=domain,
        url=url,
        license=license,
        use_cases=use_cases,
        notes=notes,
        size_hint=size_hint,
        recommended_for=recommended_for,
    )


